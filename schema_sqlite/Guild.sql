CREATE TABLE IF NOT EXISTS `Guild` (`GuildID` VARCHAR(255) DEFAULT NULL COLLATE NOCASE, 
`GuildName` VARCHAR(255) DEFAULT NULL COLLATE NOCASE, 
`Realm` UNSIGNED TINYINT(3) NOT NULL DEFAULT 0, 
`GuildBanner` TINYINT(1) NOT NULL DEFAULT 0, 
`GuildBannerLostTime` DATETIME NOT NULL DEFAULT '2000-01-01 00:00:00', 
`Motd` TEXT DEFAULT NULL COLLATE NOCASE, 
`oMotd` TEXT DEFAULT NULL COLLATE NOCASE, 
`AllianceID` VARCHAR(255) DEFAULT NULL COLLATE NOCASE, 
`Emblem` INT(11) NOT NULL DEFAULT 0, 
`RealmPoints` BIGINT(20) NOT NULL DEFAULT 0, 
`BountyPoints` BIGINT(20) NOT NULL DEFAULT 0, 
`Webpage` TEXT DEFAULT NULL COLLATE NOCASE, 
`Email` TEXT DEFAULT NULL COLLATE NOCASE, 
`Dues` TINYINT(1) NOT NULL DEFAULT 0, 
`Bank` DOUBLE NOT NULL DEFAULT 0, 
`DuesPercent` BIGINT(20) NOT NULL DEFAULT 0, 
`HaveGuildHouse` TINYINT(1) NOT NULL DEFAULT 0, 
`GuildHouseNumber` INT(11) NOT NULL DEFAULT 0, 
`GuildLevel` BIGINT(20) NOT NULL DEFAULT 0, 
`BonusType` UNSIGNED TINYINT(3) NOT NULL DEFAULT 0, 
`BonusStartTime` DATETIME NOT NULL DEFAULT '2000-01-01 00:00:00', 
`MeritPoints` BIGINT(20) NOT NULL DEFAULT 0, 
`LastTimeRowUpdated` DATETIME NOT NULL DEFAULT '2000-01-01 00:00:00', 
`Guild_ID` VARCHAR(255) NOT NULL DEFAULT '' COLLATE NOCASE, 
PRIMARY KEY (`Guild_ID`));
CREATE UNIQUE INDEX `U_Guild_GuildID` ON `Guild` (`GuildID`);
CREATE INDEX `I_Guild_GuildName` ON `Guild` (`GuildName`);
CREATE INDEX `I_Guild_AllianceID` ON `Guild` (`AllianceID`);
